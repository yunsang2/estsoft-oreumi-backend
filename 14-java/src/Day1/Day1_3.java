package day1;

public class Day1_3 {
    public static void main(String[] args) {
        int x = 10, y = 3;

        System.out.printf("x = %d, y = %d\n", x, y);
        System.out.printf("x + y = %d\n", x + y);
        System.out.printf("x - y = %d\n", x - y);
        System.out.printf("x * y = %d\n", x * y);
        System.out.printf("x / y = %.2f\n", (double)x / y);
        System.out.printf("x %% y = %d\n", x % y);
    }
}